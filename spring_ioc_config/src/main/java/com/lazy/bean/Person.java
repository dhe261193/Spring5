package com.lazy.bean;

import org.springframework.beans.factory.annotation.Value;

public class Person {

    @Value("波吉")
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                '}';
    }
}
